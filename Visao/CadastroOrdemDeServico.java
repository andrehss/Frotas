    private void botaoSalvarOrdemDeServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarOrdemDeServicoActionPerformed

       MotoristaModelo motorista=new MotoristaModelo();
      
        motorista.setCodMotorista(Integer.parseInt(campoCodigoOrdemDeServico.getText()));
        motorista.setNomeMotorista(campoCodigoServicoOrdemDeServico.getText());
        motorista.setCategoriaCNH(campoCodigoClienteOrdemDeServico.getText());
        motorista.setNumeroCNH(Integer.parseInt(campoDataEntradaOrdemDeServico.getText()));
        motorista.setVencimentoCNH(Date.valueOf(campoDataDeEntregaOrdemDeServico.getText()));
        
        try {
             Registry registry = LocateRegistry.getRegistry("127.0.0.1",1100);
             IMotoristaControle controle = (IMotoristaControle) registry.lookup("Motorista");
             String resultado =  controle.inserirMotorista(motorista);
             System.out.println(resultado);
        } catch (RemoteException ex) {
            Logger.getLogger(CadastroOrdemDeServico.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(CadastroOrdemDeServico.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_botaoSalvarOrdemDeServicoActionPerformed

