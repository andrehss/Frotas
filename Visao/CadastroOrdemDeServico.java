    private void botaoPesquisarOrdemDeServicoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarOrdemDeServicoActionPerformed
       
       campoCodigoServicoOrdemDeServico.setEnabled(true);
       campoCodigoClienteOrdemDeServico.setEnabled(true);
       campoDataEntradaOrdemDeServico.setEnabled(true);
       campoDataDeEntregaOrdemDeServico.setEnabled(true);
        
       MotoristaModelo motorista= new MotoristaModelo();
       motorista.setCodMotorista(Integer.parseInt(campoCodigoOrdemDeServico.getText()));
       
       
       try {
             Registry registry = LocateRegistry.getRegistry("127.0.0.1",1100);
             IMotoristaControle controle = (IMotoristaControle) registry.lookup("Motorista");
             motorista =  controle.selecionarMotorista(motorista);
             
             campoCodigoServicoOrdemDeServico.setText(String.valueOf(motorista.getNomeMotorista()));
             campoCodigoClienteOrdemDeServico.setText(String.valueOf(motorista.getCategoriaCNH()));
             campoDataEntradaOrdemDeServico.setText(String.valueOf(motorista.getNumeroCNH()));
             campoDataDeEntregaOrdemDeServico.setText(String.valueOf(motorista.getVencimentoCNH()));
             
        } catch (RemoteException ex) {
            Logger.getLogger(CadastroOrdemDeServico.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(CadastroOrdemDeServico.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
       
    }//GEN-LAST:event_botaoPesquisarOrdemDeServicoActionPerformed

