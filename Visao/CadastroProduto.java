    private void botaoPesquisarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarProdutoActionPerformed
         
       campoDescricaoProduto.setEnabled(true);
       campoPrecoProduto.setEnabled(true);
        
       ProdutoModelo p= new ProdutoModelo();
       p.setCodProd(Integer.parseInt(campoCodigoProduto.getText()));
      
        try {
             Registry registry = LocateRegistry.getRegistry("127.0.0.1",1100);
             IVeiculoControle controle = (IVeiculoControle) registry.lookup("Produto");
             p =  controle.selecionarProduto(p);
             campoDescricaoProduto.setText(p.getDescricaoPD());
             campoPrecoProduto.setText(String.valueOf(p.getPreco()));
                          
        } catch (RemoteException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_botaoPesquisarProdutoActionPerformed

