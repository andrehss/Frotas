    private void botaoSalvarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarProdutoActionPerformed
        
        ProdutoModelo p=new ProdutoModelo();
      
        p.setCodProd(Integer.parseInt(campoCodigoProduto.getText()));
        p.setDescricaoPD(campoDescricaoProduto.getText());
        p.setPreco(Float.parseFloat(campoPrecoProduto.getText()));
       
        try {
             Registry registry = LocateRegistry.getRegistry("127.0.0.1",1100);
             IVeiculoControle controle = (IVeiculoControle) registry.lookup("Produto");
             String resultado =  controle.inserirProduto(p);
             System.out.println(resultado);
        } catch (RemoteException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (NotBoundException ex) {
            Logger.getLogger(CadastroProduto.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_botaoSalvarProdutoActionPerformed

